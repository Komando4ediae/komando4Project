class Forecast {
  constructor(
    temperatureCelcius,
    humidityPercentage,
    windSpeed,
    precipitationAmount
  ) {
    this.temperatureCelcius = temperatureCelcius;
    this.humidityPercentage = humidityPercentage;
    this.windSpeed = windSpeed;
    this.precipitationAmount = precipitationAmount;
  }
}
